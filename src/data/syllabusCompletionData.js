export function buildSyllabusData() {
  const data = [];
  for (const sem of SEMESTER_COURSES) {
    for (const code of sem.courses) {
      const topics = SYLLABUS_TOPICS[code];
      if (topics) {
        data.push({
          course: code,
          name: getCourseName(code),
          semester: sem.semester,
          topics: [...topics],
          completed: new Array(topics.length).fill(false),
          progress: 0,
        });
      }
    }
  }
  return data;
}
