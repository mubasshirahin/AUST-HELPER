function getCourses(department = null) {
  if (department && AUST_COURSES[department]) {
    return AUST_COURSES[department];
  }
  return Object.values(AUST_COURSES).flat();
}

export function searchCourses(query, department = null, limit = 10) {
  if (!query || query.trim() === '') return [];

  const courses = getCourses(department);
  const searchTerm = query.toLowerCase().trim();

  return courses
    .filter(course =>
      course.code.toLowerCase().includes(searchTerm) ||
      course.name.toLowerCase().includes(searchTerm)
    )
    .slice(0, limit);
}

export function findCourseByCode(code, department = null) {
  if (!code) return undefined;
  const courses = getCourses(department);
  return courses.find(course =>
    course.code.toLowerCase() === code.toLowerCase().trim()
  );
}

export function findCourseByName(name, department = null) {
  if (!name) return undefined;
  const courses = getCourses(department);
  return courses.find(course =>
    course.name.toLowerCase() === name.toLowerCase().trim()
  );
}

const CSE_SEMESTER_RANGES = [
  { start: 0, end: 8, semester: 1 },
  { start: 9, end: 17, semester: 2 },
  { start: 18, end: 26, semester: 3 },
  { start: 27, end: 35, semester: 4 },
  { start: 36, end: 44, semester: 5 },
  { start: 45, end: 54, semester: 6 },
  { start: 55, end: 62, semester: 7 },
  { start: 63, end: CSE_COURSES.length - 1, semester: 8 },
];

export function getCourseSemester(code, department = null) {
  if (!code) return null;

  const courses = department === 'CSE' ? CSE_COURSES : getCourses(department);
  const ranges = department === 'CSE' ? CSE_SEMESTER_RANGES : null;

  const idx = courses.findIndex(c => c.code.toLowerCase() === code.toLowerCase().trim());
  if (idx === -1) return null;

  if (ranges) {
    for (const range of ranges) {
      if (idx >= range.start && idx <= range.end) return range.semester;
    }
  }

  return null;
}
