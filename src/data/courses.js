/**
 * Search courses by code or name
 * @param {string} query - Search query (course code or name)
 * @param {number} limit - Maximum number of results to return
 * @returns {Array} - Array of matching courses
 */
export function searchCourses(query, limit = 10) {
  if (!query || query.trim() === '') return [];
  
  const searchTerm = query.toLowerCase().trim();
  
  return AUST_COURSES
    .filter(course => 
      course.code.toLowerCase().includes(searchTerm) ||
      course.name.toLowerCase().includes(searchTerm)
    )
    .slice(0, limit);
}

/**
 * Find a course by its code
 * @param {string} code - Course code
 * @returns {Object|undefined} - Course object or undefined if not found
 */
export function findCourseByCode(code) {
  if (!code) return undefined;
  return AUST_COURSES.find(course => 
    course.code.toLowerCase() === code.toLowerCase().trim()
  );
}

/**
 * Find a course by its name
 * @param {string} name - Course name
 * @returns {Object|undefined} - Course object or undefined if not found
 */
export function findCourseByName(name) {
  if (!name) return undefined;
  return AUST_COURSES.find(course => 
    course.name.toLowerCase() === name.toLowerCase().trim()
  );
}